"use strict";

class WaitMask extends ObjectFactory {
  /**
   * 构造函数
   */
  constructor() {
    ////////////////////////////////////////////////////////////////////////////
    // 父类构造函数。
    ////////////////////////////////////////////////////////////////////////////
    super();
  }

  /**
   * 生成代码
   */
  generateCode() {
    ////////////////////////////////////////////////////////////////////////////
    // 属性代码。
    ////////////////////////////////////////////////////////////////////////////
    let attributeCode = "";
    Toolkit.eachJSONObjectKV(this.objectAttribute, function(key, value) {
      attributeCode += `${key} = "${value}"`;
    });
    this.objectCode = `
      <table id = "${this.objectId}" ${attributeCode}></table>
    `;
  }

  /**
   * 显示
   */
  show() {
    ////////////////////////////////////////////////////////////////////////////
    // [本地资源]当前对象。
    ////////////////////////////////////////////////////////////////////////////
    const thisObj = this;
    ////////////////////////////////////////////////////////////////////////////
    // 设置内容。
    ////////////////////////////////////////////////////////////////////////////
    this.getObject().html(`<tbody><tr><td><img /></td></tr></tbody>`);
    ////////////////////////////////////////////////////////////////////////////
    // 显示提示框。
    ////////////////////////////////////////////////////////////////////////////
    this.getObject().css("display", "table");
  }

  /**
   * 隐藏
   */
  hide() {
    ////////////////////////////////////////////////////////////////////////////
    // 隐藏提示框。
    ////////////////////////////////////////////////////////////////////////////
    this.getObject().css("display", "none");
  }
}
