"use strict";

/**
 * 版权
 */
class Copyright extends ObjectFactory {
  /**
   * 构造函数
   */
  constructor() {
    ////////////////////////////////////////////////////////////////////////////
    // 父类构造函数。
    ////////////////////////////////////////////////////////////////////////////
    super();
    ////////////////////////////////////////////////////////////////////////////
    // 版权内容。
    ////////////////////////////////////////////////////////////////////////////
    this.content = `&copy; ${Toolkit.getCurrentYear(true)} BlackPearl`;
  }

  /**
   * 生成代码
   */
  generateCode() {
    ////////////////////////////////////////////////////////////////////////////
    // 属性代码。
    ////////////////////////////////////////////////////////////////////////////
    let attributeCode = "";
    Toolkit.eachJSONObjectKV(this.objectAttribute, function(key, value) {
      attributeCode += `${key} = "${value}"`;
    })
    this.objectCode = `
      <div id = "${this.objectId}" ${attributeCode}>
        ${this.content}
      </div>
    `;
  }
}
