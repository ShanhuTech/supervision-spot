  /**
   * 添加角色AM确认按钮click事件
   * @param event 事件对象
   */
  addRoleAMConfirmButtonClickEvent(event) {
    ////////////////////////////////////////////////////////////////////////////
    // 获取调用源。
    ////////////////////////////////////////////////////////////////////////////
    const source = event.data;
    ////////////////////////////////////////////////////////////////////////////
    // 校验数据之前，先要隐藏之前的提示。
    ////////////////////////////////////////////////////////////////////////////
    source.addRoleAM.formList.hideAllPrompt();
    ////////////////////////////////////////////////////////////////////////////
    // 参数检查数组。
    ////////////////////////////////////////////////////////////////////////////
    const parameterCheckArray = new Array();
    parameterCheckArray.push({"name": "name", "value": source.addRoleAM.nameTextField.getObject().val(), "id": source.addRoleAM.nameTextField.getId(), "allow_null": false, "custom_error_message": null});
    parameterCheckArray.push({"name": "order", "value": source.addRoleAM.orderTextField.getObject().val(), "id": source.addRoleAM.orderTextField.getId(), "allow_null": false, "custom_error_message": null});
    if (0 < source.addRoleAM.descriptionTextField.getObject().val().length) {
      parameterCheckArray.push({"name": "description", "value": source.addRoleAM.descriptionTextField.getObject().val(), "id": source.addRoleAM.descriptionTextField.getId(), "allow_null": true, "custom_error_message": null});
    }
    ////////////////////////////////////////////////////////////////////////////
    // 检查参数。
    ////////////////////////////////////////////////////////////////////////////
    for (let i = 0; i < parameterCheckArray.length; i++) {
      const parameterObj = parameterCheckArray[i];
      if (!Module.checkParameter(source.roleRule, "addRole", parameterObj, source, function error(source, errorMessage) {
        source.addRoleAM.formList.showPrompt(parameterObj.id, errorMessage);
      })) {
        return;
      }
    }
    ////////////////////////////////////////////////////////////////////////////
    // 参数数组。
    ////////////////////////////////////////////////////////////////////////////
    const parameterArray = new Array();
    parameterArray.push({"Account-Token": AccountSecurity.getItem("account_token")});
    for (let i = 0; i < parameterCheckArray.length; i++) {
      const parameter = parameterCheckArray[i];
      const param = {};
      param[parameter.name] = parameter.value;
      parameterArray.push(param);
    }
    ////////////////////////////////////////////////////////////////////////////
    // 添加角色。
    ////////////////////////////////////////////////////////////////////////////
    Network.request(Network.RequestType.POST, Network.ResponseType.JSON, [{"Content-Type": "application/x-www-form-urlencoded"}],
      Configure.getServerUrl() + "/module/security.Role/addRole", parameterArray, source,
      function loadStart(xhr, xhrEvent, source) {
        source.addRoleAM.formList.hideResultInfo(); // 隐藏结果信息。
        source.addRoleAM.frozenControl("addRole"); // 冻结控件。
      },
      function error(xhr, xhrEvent, source) {
        source.addRoleAM.formList.showResultInfo("error", "网络请求失败");
      },
      function timeout(xhr, xhrEvent, source) {
        source.addRoleAM.formList.showResultInfo("error", "网络请求超时");
      },
      function readyStateChange(xhr, xhrEvent, source) {
        if ((XMLHttpRequest.DONE == xhr.readyState) && (200 == xhr.status)) {
          source.addRoleAM.recoverControl("addRole"); // 恢复控件。
          //////////////////////////////////////////////////////////////////////
          // 响应结果。
          //////////////////////////////////////////////////////////////////////
          const responseResult = xhr.response;
          if (Toolkit.stringEqualsIgnoreCase("SUCCESS", responseResult.status)) {
            ////////////////////////////////////////////////////////////////////
            // 显示成功信息。
            ////////////////////////////////////////////////////////////////////
            source.addRoleAM.formList.showResultInfo("success", "添加成功");
            ////////////////////////////////////////////////////////////////////
            // 重置控件。
            ////////////////////////////////////////////////////////////////////
            source.addRoleAM.resetControl();
            ////////////////////////////////////////////////////////////////////
            // 获取角色。
            ////////////////////////////////////////////////////////////////////
            source.getRole();
          } else if (Toolkit.stringEqualsIgnoreCase("ERROR", responseResult.status)) {
            source.addRoleAM.formList.showResultInfo("error", responseResult.attach);
          } else {
            source.addRoleAM.formList.showResultInfo("error", "操作异常");
          }
        }
      }
    );
  }

