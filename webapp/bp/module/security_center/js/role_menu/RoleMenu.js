    // 工具栏添加角色名称标签。
    ////////////////////////////////////////////////////////////////////////////
    this.toolbar.getObject().append(this.roleNameLabel.getCode());
    ////////////////////////////////////////////////////////////////////////////
    // 工具栏添加角色名称下拉框。
    ////////////////////////////////////////////////////////////////////////////
    this.toolbar.getObject().append(this.roleNameSelect.getCode());
    ////////////////////////////////////////////////////////////////////////////
    // 工具栏添加保存按钮。
    ////////////////////////////////////////////////////////////////////////////
    this.toolbar.getObject().append(this.saveButton.getCode());
    ////////////////////////////////////////////////////////////////////////////
    // 容器添加菜单表格。
    ////////////////////////////////////////////////////////////////////////////
