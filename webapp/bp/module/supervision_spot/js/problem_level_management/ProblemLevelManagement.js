  /**
   * 获取问题类型
   */
  getProblemLevel() {
    ////////////////////////////////////////////////////////////////////////////
    // 获取问题类型。
    ////////////////////////////////////////////////////////////////////////////
    Network.request(Network.RequestType.POST, Network.ResponseType.JSON, [{"Content-Type": "application/x-www-form-urlencoded"}],
      Configure.getServerUrl() + "/module/supervision.spot.ProblemLevel/getProblemLevel", [{"Account-Token": AccountSecurity.getItem("account_token")}], this,
      function loadStart(xhr, xhrEvent, source) {
        source.frozenControl("getProblemLevel"); // 冻结控件。
        source.waitMask.show(); // 显示等待遮蔽。
      },
      function error(xhr, xhrEvent, source) {
        Error.redirect("../home/error.html", "获取问题类型", "网络请求失败", window.location.href);
      },
      function timeout(xhr, xhrEvent, source) {
        Error.redirect("../home/error.html", "获取问题类型", "网络请求超时", window.location.href);
      },
      function readyStateChange(xhr, xhrEvent, source) {
        if ((XMLHttpRequest.DONE == xhr.readyState) && (200 == xhr.status)) {
          source.recoverControl("getProblemLevel"); // 恢复控件。
          source.waitMask.hide(); // 隐藏等待遮蔽。
          //////////////////////////////////////////////////////////////////////
          // 响应结果。
          //////////////////////////////////////////////////////////////////////
          const responseResult = xhr.response;
          if (Toolkit.stringEqualsIgnoreCase("SUCCESS", responseResult.status)) {
            ////////////////////////////////////////////////////////////////////
            // 清空数组。
            ////////////////////////////////////////////////////////////////////
            source.problemLevelArray.splice(0, source.problemLevelArray.length);
            ////////////////////////////////////////////////////////////////////
            // 更新数组。
            ////////////////////////////////////////////////////////////////////
            for (let i = 0; i < responseResult.content.array.length; i++) {
              source.problemLevelArray.push(responseResult.content.array[i]);
            }
            ////////////////////////////////////////////////////////////////////
            // 恢复控件。
            ////////////////////////////////////////////////////////////////////
            source.recoverControl("getProblemLevel");
            ////////////////////////////////////////////////////////////////////
            // 如果问题类型数组不为空，则添加表格数据。
            ////////////////////////////////////////////////////////////////////
            if (0 < source.problemLevelArray.length) {
              let code = "";
              for (let i = 0; i < source.problemLevelArray.length; i++) {
                const problemLevel = source.problemLevelArray[i];
                code += `
                  <tr data-order = "${problemLevel.order}">
                    <td class = "name">${problemLevel.name}</td>
                    <td class = "operation" data-uuid = "${problemLevel.uuid}"><span class = "modify">修改</span><span class = "remove">删除</span></td>
                  </tr>
                `;
              }
              source.problemLevelTable.getObject().find("tbody").html(code);
            } else {
              source.problemLevelTable.getObject().find("tbody").html(`
                <tr>
                  <td class = "rowspan" colspan = "2">尚无数据</td>
                </tr>
              `);
            }
            ////////////////////////////////////////////////////////////////////
            // 加载完成后注册事件。
            ////////////////////////////////////////////////////////////////////
            source.problemLevelTable.getObject().find("tbody").find("tr").find(".operation").find(".modify").off("click").on("click", null, source, source.problemLevelTableModifyButtonClickEvent);
            source.problemLevelTable.getObject().find("tbody").find("tr").find(".operation").find(".remove").off("click").on("click", null, source, source.problemLevelTableRemoveButtonClickEvent);
          } else {
            Error.redirect("../home/error.html", "获取问题类型", responseResult.attach, window.location.href);
          }
        }
      }
    );
  }

  /**
   * 冻结控件
   * @param name 冻结标记名称
   */
  frozenControl(name) {
    ////////////////////////////////////////////////////////////////////////////
    // 存入队列。
    ////////////////////////////////////////////////////////////////////////////
    this.queue.push(name);
    this.addProblemLevelButton.getObject().attr("disabled", "disabled");
  }

  /**
   * 恢复控件
   * @param name 恢复标记名称
   */
  recoverControl(name) {
    ////////////////////////////////////////////////////////////////////////////
    // 队列取出。
    ////////////////////////////////////////////////////////////////////////////
    this.queue.pop(name);
    if (this.queue.isEmpty()) {
      //////////////////////////////////////////////////////////////////////////
      // 如果取出了队列中所有的元素才能恢复。
      //////////////////////////////////////////////////////////////////////////
      this.addProblemLevelButton.getObject().removeAttr("disabled");
    }
  }
